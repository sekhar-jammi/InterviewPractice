package com.inheritance;

public class Switchcase {
 
	   static int score=1;
	   public static void main(String[] args) {
		  
              switch(score) {
                     case 1:
        	            System.out.println("score is 1.");
                        break;
                     case 2:
        	            System.out.println("score is 2.");
        	            break;
                     default:
        	            System.out.println("score is default.");
           } 
	}

}
